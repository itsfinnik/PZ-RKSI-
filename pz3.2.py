# Ввод: номер единицы массы (1-5) и масса тела
n = int(input())  # номер единицы: 1-кг, 2-мг, 3-г, 4-т, 5-ц
m = float(input())  # масса в указанных единицах

# Конвертируем в килограммы в зависимости от номера единицы
if n == 1:
    result = m  # уже в килограммах
elif n == 2:
    result = m / 1000000  # миллиграммы в килограммы (1 кг = 1 000 000 мг)
elif n == 3:
    result = m / 1000  # граммы в килограммы (1 кг = 1000 г)
elif n == 4:
    result = m * 1000  # тонны в килограммы (1 т = 1000 кг)
elif n == 5:
    result = m * 100  # центнеры в килограммы (1 ц = 100 кг)

# Вывод: масса в килограммах
print(result)