# Ввод: процент увеличения пробега P (вещественное число от 0 до 50)
P = float(input())

# Начальные условия первого дня тренировок
day_distance = 10  # пробег в текущий день (начальный - 10 км)
total_distance = 10  # суммарный пробег за все дни
K = 1  # счетчик дней (уже прошел 1 день)

# Цикл продолжается пока суммарный пробег не превысит 200 км
while total_distance <= 200:
    # Увеличиваем дневной пробег на P процентов от предыдущего дня
    day_distance += day_distance * (P / 100)
    # Добавляем новый дневной пробег к общему суммарному пробегу
    total_distance += day_distance
    # Увеличиваем счетчик дней на 1
    K += 1

# Вывод: количество дней K и суммарный пробег S (округленный до 2 знаков)
print(K)
print(round(total_distance, 2))