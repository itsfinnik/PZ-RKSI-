# Ввод: два целых числа A и B, где A < B
A = int(input())  # начало диапазона
B = int(input())  # конец диапазона

# Инициализация переменной для накопления произведения
product = 1

# Перебираем все целые числа от A до B включительно
for i in range(A, B + 1):
    # Умножаем текущее значение произведения на очередное число
    product *= i

# Вывод: произведение всех чисел от A до B
print(product)